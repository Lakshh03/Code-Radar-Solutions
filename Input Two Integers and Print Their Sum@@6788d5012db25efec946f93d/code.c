#include <stdio.h>

int main() {
    int num1, num2, sum;
    
    // Asking for user input
    printf(" ");
    
    // Reading the two integers
    scanf("%d %d", &num1, &num2);
    
    // Calculating the sum
    sum = num1 + num2;
    
    // Printing the sum
    printf("Sum: %d\n", sum);
    
    return 0;
}
